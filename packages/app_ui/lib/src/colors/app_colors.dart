import 'package:flutter/material.dart';

/// Defines the color palette for the App UI Kit.
abstract class AppColors {
  /// White
  static const white = Color(0XFFFFFFFF);

  /// Black
  static const black = Color(0XFF000000);

  /// Transparent
  static const transparent = Color(0X00000000);

  /// The default disabled foreground color.
  static const disabledForeground = Color(0x611B1B1B);

  /// The default disabled button color.
  static const disabledButton = Color(0x1F000000);

  /// The pastel grey color.
  static const pastelGrey = Color(0xFFCCCCCC);

  /// Light black
  static const lightBlack = Colors.black54;

  /// Grey
  static const blueCharcoal = Color(0xFF1D2228);
}
